import requests
import re

def fetch_wmts_tile_auto(layer_id, z, x, y, output_filename):
    """
    動態解析 WMTS XML 目錄，自動找出該圖層的真實 RESTful 路徑並下載
    """
    # 取得服務目錄清單
    caps_url = "https://www.historygis.udd.gov.taipei/WMTS/"
    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/114.0.0.0 Safari/537.36",
        "Referer": "https://historygis.udd.gov.taipei/"
    }

    print("向伺服器索取總目錄清單...")
    try:
        res = requests.get(caps_url, headers=headers)
        if res.status_code != 200:
            print("無法取得目錄，伺服器可能阻擋了連線。")
            return
    except Exception as e:
        print(f"連線失敗：{e}")
        return

    # 2. 找出該圖層專屬的 RESTful 模板網址
    print(f"正在解析圖層 {layer_id} 的真實路徑...")

    # 為了避免跨圖層讀取錯誤，用 <Layer> 標籤把 XML 切開
    layers_data = res.text.split("<Layer>")
    target_template = None

    for layer_xml in layers_data:
        # 如果這個區塊裡面有 Layer ID
        if f"<ows:Identifier>{layer_id}</ows:Identifier>" in layer_xml:
            # 把 template="..." 裡面的網址抓出來
            match = re.search(r'template="([^"]+)"', layer_xml)
            if match:
                target_template = match.group(1)
                break

    if not target_template:
        print(f"失敗：在伺服器的目錄中找不到圖層 {layer_id} 的網址格式！")
        return

    print(f"找到伺服器規定的模板：\n   {target_template}")

    # 替換掉括號裡的變數
    final_url = target_template.replace("{Style}", "default") \
                               .replace("{TileMatrixSet}", "GoogleMapsCompatible") \
                               .replace("{TileMatrix}", str(z)) \
                               .replace("{TileRow}", str(y)) \
                               .replace("{TileCol}", str(x))

    print(f"最終網址: {final_url}")

    # 真正去下載圖片
    try:
        img_res = requests.get(final_url, headers=headers)

        if img_res.status_code == 200 and 'image' in img_res.headers.get('Content-Type', ''):
            with open(output_filename, 'wb') as f:
                f.write(img_res.content)
            print(f"圖片成功下載並儲存至：{output_filename}")
        else:
            print(f"圖片下載失敗。狀態碼: {img_res.status_code}")
            print(f"伺服器回應：{img_res.text[:200]}")
    except Exception as e:
        print(f"下載圖片時發生錯誤：{e}")

