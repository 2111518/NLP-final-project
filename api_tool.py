import os
from geopy.geocoders import Nominatim # 這是把地名轉經緯度的套件 (需 pip install geopy)

def latlon_to_tile_xy(lat, lon, zoom=11):
    # 座標轉換公式，用來算出 X, Y
    n = 2.0 ** zoom
    xtile = int((lon + 180.0) / 360.0 * n)
    ytile = int((1.0 - math.asinh(math.tan(math.radians(lat))) / math.pi) / 2.0 * n)
    return xtile, ytile

#location_name:中文或英文
def get_taipei_historical_info(location_name: str) -> str:
    # 將地名轉為經緯度 (模擬查 Google Map)
    geolocator = Nominatim(user_agent="taipei_history_bot")
    location = geolocator.geocode(f"台北市 {location_name}")

    if not location:
        return json.dumps({"error": f"找不到 {location_name} 的座標"})

    lat, lon = location.latitude, location.longitude

    # 轉換為 ArcGIS 的圖磚 X, Y 座標
    zoom_level = 16 # 越高等越詳細
    x, y = latlon_to_tile_xy(lat, lon, zoom_level)

    # 組裝不同年份的網址
    base_url = "https://www.historygis.udd.gov.taipei/arcgis/rest/services/Aerial/{layer}/MapServer/tile/{z}/{y}/{x}"

    result = {
        "status": "success",
        "location": location_name,
        "zoom": zoom_level,
        "historical_images": {
            "1979": base_url.format(layer="Ortho_1979_2", z=zoom_level, y=y, x=x),
            "2005": base_url.format(layer="Ortho_2005", z=zoom_level, y=y, x=x),
            "2023": base_url.format(layer="Ortho_2023", z=zoom_level, y=y, x=x)
        }
    }

    # 把字典轉成 JSON 字串，交還給 Agent
    return json.dumps(result)

